# 숫자 연산자
print(10 + 10)  # 덧셈
print(10 - 10)  # 뺄셈
print(10 * 10)  # 곱셈
print(10 / 10)  # 나눗셈
print(10 // 10) # 정수 나눗셈

print(10 % 10)  # 나머지
print(10 ** 10) # 제곱 
print()
print()

# 부동소수점끼리의 연산
