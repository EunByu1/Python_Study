print(10.0 - 10.0)
print(10.0 * 10.0)
print(10.0 / 10.0)
print(10.0 // 10.0)

print(10.0 % 10.0)
print(10.0 ** 10.0)
print()
print()

print(10 // 3)
print(10.0 // 3)
print(10 // 3.0)
